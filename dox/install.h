/*! \page install How to Build and Install MadButterfly?
 *
 * Prerequisite
 * - install pmake (pmake is make of FreeBSD)
 * - install Cairo
 *
 * Get source
 * - hg clone http://hg.assembla.com/MadButterfly MadButterfly
 *
 * Build and Install
 * - cd MadButtfly
 * - make
 * - make install
 *
 * Examples
 * - cd examples/calculator
 * - make
 * - ./calc
 *
 * - cd examples/svg2code_ex
 * - make
 * - ./ex1
 *
 * - cd examples/tank
 * - make
 * - ./tank
 */
